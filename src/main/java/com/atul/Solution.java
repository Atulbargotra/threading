package com.atul;

import java.util.HashSet;

class Solution {
    public int pivotIndex(int[] nums) {
        String s = "atul";
        String t = "verm";
        HashSet<Character> hs = new HashSet<>();
        HashSet<Character> ht = new HashSet<>();
        for(int i=0; i<s.length(); i++){
            hs.add(s.charAt(i));
        }
        for(int i=0; i<t.length(); i++){
            ht.add(t.charAt(i));
        }

        return 1;
    }
}
